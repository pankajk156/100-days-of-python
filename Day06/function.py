## Extra tip:

## What is function in python?
## A function is a block of organized, reusable code that is used to perform a single, related action. Functions provide better modularity for your application and a high degree of code reusing.
## Python gives you many built-in functions like print(), etc. but you can also create your own functions. These functions are called user-defined functions.

## How to create own function in python?
## Function blocks begin with the keyword def followed by the function name and parentheses ( ( ) ). The code block within every function starts with a colon (:) and is indented.

## Indentation - Indentation refers to the spaces at the beginning of a code line. Where in other programming languages the indentation in code is for readability only, the indentation in Python is very important.
## Python uses indentation to indicate a block of code.

## What is while loop?
## The while loop in Python is used to iterate over a block of code as long as the test expression (condition) is true.

## What is Infinite loop?
## An Infinite Loop in Python is a continuous repetitive conditional loop that gets executed until an external factor interferes in the execution flow, like insufficient CPU memory,
## a failed feature/ error code that stopped the execution, or a new feature in the other legacy systems that needs code integration.